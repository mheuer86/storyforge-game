const BIBLES_BY_GENRE: Record<string, string> = {
  generic: SF2_BIBLE_HEGEMONY,
  hegemony: SF2_BIBLE_HEGEMONY,
  'space-opera': SF2_BIBLE_SPACE_OPERA,
  fantasy: SF2_BIBLE_FANTASY,
  cyberpunk: SF2_BIBLE_CYBERPUNK,
  grimdark: SF2_BIBLE_GRIMDARK,
  noire: SF2_BIBLE_NOIR,
}

function resolveGenreId(genreId?: string): string {
  if (genreId && BIBLES_BY_GENRE[genreId] && EXAMPLES_BY_GENRE[genreId]) {
    return genreId
  }
  return 'hegemony'
}

export function getSf2GenreNarrativeProfile(
  genreId?: string
): Sf2GenreNarrativeProfile {
  const id = resolveGenreId(genreId)
  return {
    id,
    bible: BIBLES_BY_GENRE[id],
    examples: EXAMPLES_BY_GENRE[id],
  }
}

export function getSf2BibleForGenre(genreId?: string): string {
  return getSf2GenreNarrativeProfile(genreId).bible
}

export function getSf2GenreExamples(genreId?: string): Sf2GenreExamples {
  return getSf2GenreNarrativeProfile(genreId).examples
}
